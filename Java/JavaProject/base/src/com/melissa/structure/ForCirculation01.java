package com.melissa.structure;

public class ForCirculation01 {
    public static void main(String[] args) {
        //while循环
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        System.out.println("=========================================================================================");

        //for循环
        for (int j=1; j<=10; j++){
            System.out.println(j);
        }
    }
}
